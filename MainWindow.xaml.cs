        static private void WriteJointsToJson(IReadOnlyDictionary<JointType, Joint> joints)
        {
            float[] JointList(JointType jointType)
            {
                if (currentJoints != null)
                {
                    return new float[] { joints[jointType].Position.X, joints[jointType].Position.Y, joints[jointType].Position.Z };
                }
                else
                {
                    return new float[] { 0, 0, 0 };
                }
            }

            JsonData data = new JsonData()
            {
                rAnkle = JointList(JointType.AnkleRight),
                rKnee = JointList(JointType.KneeRight),
                rHip = JointList(JointType.HipRight),
                lHip = JointList(JointType.HipLeft),
                lKnee = JointList(JointType.KneeLeft),
                lAnkle = JointList(JointType.AnkleLeft),
                pelvis = JointList(JointType.SpineBase),
                midSpine = JointList(JointType.SpineMid),
                neck = JointList(JointType.Neck),
                head = JointList(JointType.Head),
                rWrist = JointList(JointType.WristRight),
                rElbow = JointList(JointType.ElbowRight),
                rShoulder = JointList(JointType.ShoulderRight),
                lShoulder = JointList(JointType.ShoulderLeft),
                lElbow = JointList(JointType.ElbowLeft),
                lWrist = JointList(JointType.WristLeft)
            };

            jsonJointData.Add(data);
        }

        public static void SaveBitmap(WriteableBitmap wbitmap, string filename)
        {
            // Save the bitmap into a file.
            using (FileStream stream = new FileStream(filename, FileMode.Create))
            {
                PngBitmapEncoder encoder = new PngBitmapEncoder();
                encoder.Frames.Add(BitmapFrame.Create(wbitmap));
                encoder.Save(stream);
            }
        }

